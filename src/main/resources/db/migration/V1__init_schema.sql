-- TABLE: semesters
CREATE TABLE semesters (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at TIMESTAMP NOT NULL,
    updated_at TIMESTAMP NOT NULL,
    active BOOLEAN NOT NULL,
    code VARCHAR(255) NOT NULL,
    start_date DATE NOT NULL,
    end_date DATE NOT NULL,
    name VARCHAR(255) NOT NULL,
    CONSTRAINT uk_semesters_code UNIQUE (code)
);

CREATE INDEX idx_semesters_code ON semesters(code);
CREATE INDEX idx_semesters_active ON semesters(active);
CREATE INDEX idx_semesters_dates ON semesters(start_date, end_date);


-- TABLE: subjects
-- Note: V1 had 'credit'; V11 merged it into 'credits' and dropped 'credit'.
CREATE TABLE subjects (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at TIMESTAMP NOT NULL,
    updated_at TIMESTAMP NOT NULL,
    active BOOLEAN NOT NULL,
    code VARCHAR(255) NOT NULL,
    description TEXT,
    name VARCHAR(255) NOT NULL,
    -- V3: added credits, theory_hours, practice_hours, department_id, prerequisite_subject_ids
    -- V11: finalized credits as NOT NULL DEFAULT 3 (merged from legacy 'credit')
    credits INTEGER NOT NULL DEFAULT 3,
    theory_hours INTEGER DEFAULT 0,
    practice_hours INTEGER DEFAULT 0,
    department_id BIGINT,
    prerequisite_subject_ids BIGINT[],
    CONSTRAINT uk_subjects_code UNIQUE (code)
);

CREATE INDEX idx_subjects_code ON subjects(code);
CREATE INDEX idx_subjects_active ON subjects(active);
CREATE INDEX idx_subjects_department_id ON subjects(department_id);


-- TABLE: users
-- Note: 'role' column existed in V1 but was dropped in V7.
--       'avatar' added in V8.
--       Security fields added in V13.
CREATE TABLE users (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at TIMESTAMP NOT NULL,
    updated_at TIMESTAMP NOT NULL,
    email VARCHAR(255) NOT NULL,
    password VARCHAR(255) NOT NULL,
    username VARCHAR(255) NOT NULL,
    -- V8
