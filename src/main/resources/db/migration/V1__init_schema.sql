-- TABLE: courses
-- Final state after V3 additions, V10 column removals, V12 teacher FK migration, V16 enrollment window.
-- Note: V10 dropped description, thumbnail_url, status, credits, department_id,
--       duration_weeks, level, language. V16 uses enrollment_start_date/end_date,
--       so those are retained in the final schema.
--       V17 sets DEFAULT 3 on credits — but since V10 dropped credits and no later
--       migration re-adds it, the credits column is NOT included here.
CREATE TABLE courses (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at TIMESTAMP NOT NULL,
    updated_at TIMESTAMP NOT NULL,
    active BOOLEAN NOT NULL,
    code VARCHAR(255) NOT NULL,
    current_students INTEGER NOT NULL,
    max_students INTEGER NOT NULL,
    name VARCHAR(255) NOT NULL,
    semester_id BIGINT NOT NULL,
    subject_id BIGINT NOT NULL,
    -- V12: teacher_id now references teachers(id), not users(id)
    teacher_id BIGINT,
    -- V16: enrollment window columns (originally V3, retained through V16)
    enrollment_start_date DATE,
    enrollment_end_date DATE,
    CONSTRAINT uk_courses_code UNIQUE (code),
    CONSTRAINT fk_courses_semester FOREIGN KEY (semester_id) REFERENCES semesters(id),
    CONSTRAINT fk_courses_subject FOREIGN KEY (subject_id) REFERENCES subjects(id),
    CONSTRAINT fk_courses_teacher FOREIGN KEY (teacher_id) REFERENCES teachers(id) ON DELETE SET NULL
);

CREATE INDEX idx_courses_semester_id ON courses(semester_id);
CREATE INDEX idx_courses_subject_id ON courses(subject_id);
CREATE INDEX idx_courses_teacher_id ON courses(teacher_id);
CREATE INDEX idx_courses_active ON courses(active);


-- TABLE: enrollments
-- Final state after V15: student_id references students(id) instead of users(id).
CREATE TABLE enrollments (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at TIMESTAMP NOT NULL,
    updated_at TIMESTAMP NOT NULL,
    grade DOUBLE PRECISION,
    status VARCHAR(255) NOT NULL,
    course_id BIGINT NOT NULL,
    -- V15: student_id now references students(id)
    student_id BIGINT NOT NULL,
    CONSTRAINT uk_enrollments_course_student UNIQUE (course_id, student_id),
    CONSTRAINT fk_enrollments_course FOREIGN KEY (course_id) REFERENCES courses(id),
    CONSTRAINT fk_enrollments_student FOREIGN KEY (student_id) REFERENCES students(id) ON DELETE CASCADE
);

CREATE INDEX idx_enrollments_course_id ON enrollments(course_id);
CREATE INDEX idx_enrollments_student_id ON enrollments(student_id);
CREATE INDEX idx_enrollments_status ON enrollments(status);


-- TABLE: course_time_slots  (V16)
